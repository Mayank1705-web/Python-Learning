num1 = int(input("Enter first number: "))
num2 = int(input("Enter second number: "))
num3 = int(input("Enter third number: "))
if num1 + num2 + num3 == 0:
    print("The sum of the three numbers is Zero. Sum = ", num1 + num2 + num3)
else: 
    print("The sum of the three numbers is Non-Zero. Sum = ", num1 + num2 + num3)